package com.example.dedalus.textAnalyzer.model;


/**
 * The AnalysisType enumeration defines the types of analyses that can be performed on text.
 * It is used to specify the kind of character analysis to conduct in the text analysis process.
 *
 * There are two types of analysis available:
 * - VOWELS: Analysis focused on vowels in the text.
 * - CONSONANTS: Analysis focused on consonants in the text.
 */
public enum AnalysisType {
    VOWELS,
    CONSONANTS
}
